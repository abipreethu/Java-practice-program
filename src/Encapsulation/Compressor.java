package Encapsulation;

public class Compressor {
String pressor;
String brand;
int price;
int olt;

public void setPressor(String pressor) {
	this.pressor = pressor;
}
public String getPressor() {
	return pressor;
}
public void setBrand(String brand) {
	this.brand = brand;
}
public String getBrand() {
	return brand;
}

public void setPrice(int price) {
	this.price = price;
}
public int getPrice() {
	return price;
}

public void setOlt(int olt) {
	this.olt = olt;
}
public int getOlt() {
	return olt;
}

}
